#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef pair<ll, ll> pll;
int main(void) {
	ios::sync_with_stdio(false);
	cin.tie(0);
	ll num = 0;
	cin >> num;
	while (num--) {
		ll n = 0, m = 0;
		cin >> n >> m;
		for (int i = 0; i < m; i++) {
			ll start = 0, end = 0;
			cin >> start >> end;
		}
		cout << n - 1 << '\n';
	}
	return 0;
}